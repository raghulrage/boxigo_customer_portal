                        <p> 
                            Forget about searching on the internet, sharing your personal details in multiple listing websites, 
                            and talking to multiple packers and movers to get quotations. Booking your move on Boxigo is easy and 
                            saves you time and money! Just enter in your move details once to get guaranteed prices from multiple packers and movers.
                            
                            The prices you see on Boxigo are guaranteed, based on the details on your Move Plan. 
                            Prices would vary in case of change in the volume not mentioned earlier. If you book your move on Boxigo, 
                            your mover must adhere to the Boxigo Service Standards, so rest assured that your move will be handled with care and respect.
                            
                            Your friends, The Boxigo’s Move Planner and Shifting Expert, will always be here for you to guide you along the way before, during, and after your move.
                        </p>
                        
